#!/usr/bin/env bash
# nova-disk-cleanup.sh вЂ” Weekly host disk cleanup for a Docker-only server.
#
# Installed to /etc/cron.weekly/nova-disk-cleanup by host-disk-settings.sh.
# Can also be run manually: sudo bash nova-disk-cleanup.sh
#
# What it cleans:
#   1. Dangling Docker images (untagged, replaced by newer pulls)
#   2. Orphaned anonymous Docker volumes (not attached to any container)
#   3. Docker build cache older than 7 days
#   4. APT package cache
#   5. Failed login logs (btmp) вЂ” truncated, not deleted
#   6. Old temp files in /tmp and /var/tmp (>7 days)
#   7. Vacuum journald to configured limit

set -euo pipefail

log() { echo "[nova-disk-cleanup] $(date '+%Y-%m-%d %H:%M:%S') $*"; }

# Record starting usage
BEFORE=$(df / --output=used | tail -1)

log "=== Starting weekly disk cleanup ==="

# в”Ђв”Ђ 1. Docker: dangling images в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

PRUNED=$(docker image prune -f 2>/dev/null | tail -1)
log "Images: $PRUNED"

# в”Ђв”Ђ 2. Docker: orphaned anonymous volumes в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

PRUNED=$(docker volume prune -f 2>/dev/null | tail -1)
log "Volumes: $PRUNED"

# в”Ђв”Ђ 3. Docker: stale build cache в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

PRUNED=$(docker builder prune -f --filter "until=168h" 2>/dev/null | tail -1)
log "Build cache: $PRUNED"

# в”Ђв”Ђ 4. APT cache в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

apt-get clean -y >/dev/null 2>&1
log "APT cache: cleaned"

# в”Ђв”Ђ 5. Failed login logs в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

for f in /var/log/btmp /var/log/btmp.1; do
  if [[ -f "$f" ]]; then
    truncate -s 0 "$f"
  fi
done
log "btmp: truncated"

# в”Ђв”Ђ 6. Old temp files в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

TEMP_CLEANED=$(find /tmp /var/tmp -type f -atime +7 -delete -print 2>/dev/null | wc -l)
log "Temp files: removed $TEMP_CLEANED files older than 7 days"

# в”Ђв”Ђ 7. Journal vacuum в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

journalctl --vacuum-size=200M >/dev/null 2>&1
log "Journal: vacuumed to 200M limit"

# в”Ђв”Ђ Summary в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

AFTER=$(df / --output=used | tail -1)
SAVED_KB=$(( BEFORE - AFTER ))

if (( SAVED_KB > 0 )); then
  if (( SAVED_KB > 1048576 )); then
    SAVED="$(( SAVED_KB / 1048576 ))GB"
  elif (( SAVED_KB > 1024 )); then
    SAVED="$(( SAVED_KB / 1024 ))MB"
  else
    SAVED="${SAVED_KB}KB"
  fi
  log "=== Done вЂ” reclaimed ~${SAVED} ==="
else
  log "=== Done вЂ” disk already clean ==="
fi
